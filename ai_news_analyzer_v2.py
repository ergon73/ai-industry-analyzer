def collect_articles_from_rss_v2(rss_feed_urls: str) -> str:
    """–°–æ–±–∏—Ä–∞–µ—Ç —Å—Ç–∞—Ç—å–∏ –∏–∑ RSS-–ª–µ–Ω—Ç –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ"""
    articles = []
    urls = [url.strip() for url in rss_feed_urls.split(',')]

    print(f"--- [–§–£–ù–ö–¶–ò–Ø V2] –ù–∞—á–∏–Ω–∞—é —Ä–∞–±–æ—Ç—É —Å {len(urls)} RSS-–ª–µ–Ω—Ç–∞–º–∏ ---")
    
    for url in urls:
        try:
            print(f"üì° –û–±—Ä–∞–±–∞—Ç—ã–≤–∞—é RSS-–ª–µ–Ω—Ç—É: {url}")
            feed = feedparser.parse(url)
            source_name = feed.feed.title if hasattr(feed.feed, 'title') else url
            
            if not feed.entries:
                print(f"‚ö†Ô∏è  RSS-–ª–µ–Ω—Ç–∞ {url} –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç —Å—Ç–∞—Ç–µ–π")
                continue
                
            # –ë–µ—Ä–µ–º –¥–æ 15 –Ω–æ–≤–æ—Å—Ç–µ–π –∏–∑ –∫–∞–∂–¥–æ–π –ª–µ–Ω—Ç—ã
            for entry in feed.entries[:15]:
                print(f"üìÑ –°–∫–∞—á–∏–≤–∞—é —Å—Ç–∞—Ç—å—é: {entry.title} ---")
                try:
                    response = requests.get(entry.link, headers={'User-Agent': 'Mozilla/5.0'}, timeout=10)
                    response.raise_for_status()
                    soup = BeautifulSoup(response.content, 'html.parser')
                    
                    # –£–¥–∞–ª—è–µ–º –Ω–µ–Ω—É–∂–Ω—ã–µ —Ç–µ–≥–∏
                    for tag in soup(["script", "style", "nav", "footer", "header", "aside"]):
                        tag.decompose()
                    
                    # –ü—ã—Ç–∞–µ–º—Å—è –Ω–∞–π—Ç–∏ –æ—Å–Ω–æ–≤–Ω–æ–π –∫–æ–Ω—Ç–µ–Ω—Ç —Å—Ç–∞—Ç—å–∏
                    content = soup.find('article') or soup.find('div', class_='content') or soup.find('main') or soup.find('body')
                    if content:
                        text = content.get_text(separator='\n', strip=True)
                    else:
                        text = soup.get_text(separator='\n', strip=True)
                    
                    articles.append({
                        'source': source_name,
                        'title': entry.title,
                        'link': entry.link,
                        'text': text[:2000]  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –¥–ª–∏–Ω—É —Ç–µ–∫—Å—Ç–∞
                    })
                    
                except Exception as e:
                    print(f"‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —Å—Ç–∞—Ç—å—é {entry.link}: {e}")
                    continue
                    
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ RSS-–ª–µ–Ω—Ç—ã {url}: {e}")
            continue

    if not articles:
        return "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å —Å—Ç–∞—Ç—å–∏. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å RSS-–ª–µ–Ω—Ç."
        
    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º JSON —Å—Ç—Ä–æ–∫—É –¥–ª—è –ø–µ—Ä–µ–¥–∞—á–∏ –º–µ–∂–¥—É –∞–≥–µ–Ω—Ç–∞–º–∏
    return json.dumps(articles, ensure_ascii=False, indent=2)

